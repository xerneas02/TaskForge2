    public class Pokemon
    {
        public int Id { get; set; }
        public string Nom { get; set; }

        public Type Type { get; set; }

        public Nature Nature { get; set;}

        public Ability Ability { get; set;}

        public Pokemon(int id, string nom, Type type, Nature nature = Nature.Hardy, Ability ability = Ability.Magic_Guard)
        {
            Id = id;
            Nom = nom;
            Type = type;
            Nature = nature;
            Ability = ability;
        }
        
        
